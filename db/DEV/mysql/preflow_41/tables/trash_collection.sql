CREATE TABLE `trash_collection` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  `user_id` BIGINT UNSIGNED NOT NULL,
  `object_id` BIGINT UNSIGNED DEFAULT NULL,
  `object_uid` VARBINARY(1000) DEFAULT NULL,
  `object_type` VARBINARY(50) NOT NULL,
  `object_href` TEXT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci,
  `sync_token` TINYINT UNSIGNED DEFAULT '0',
  `status` TINYINT UNSIGNED NOT NULL DEFAULT '1',
  `trash_time` DOUBLE(13,3) NOT NULL DEFAULT '0.000',
  `created_date` DOUBLE(13,3) NOT NULL,
  `updated_date` DOUBLE(13,3) DEFAULT NULL,
  `category` TINYINT(1) NOT NULL DEFAULT '0',
  PRIMARY KEY (`id`),
  KEY `idx_object_type` (`object_type`) USING BTREE,
  KEY `idx_on_object_type_and_object_type` (`object_uid`,`object_type`) USING BTREE,
  KEY `idx_updated_date` (`updated_date`) USING BTREE,
  KEY `idx_user_id` (`user_id`) USING BTREE,
  UNIQUE KEY `user_id_and_object_id_and_object_type` (`user_id`,`object_id`,`object_type`) USING BTREE,
  UNIQUE KEY `user_id_and_object_uid_and_object_type` (`user_id`,`object_uid`,`object_type`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=latin1 COMMENT='latin1_swedish_ci'