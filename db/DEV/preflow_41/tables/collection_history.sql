CREATE TABLE `collection_history` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `collection_activity_id` BIGINT NOT NULL DEFAULT '0',
  `email` VARCHAR(255) NOT NULL DEFAULT '',
  `action` INT NOT NULL DEFAULT '0' COMMENT 'he action of the user changes the data of shared objects\n0: created (default )\n1: edited\n2: moved\n3: deleted\n4: completed (mark done) \n5: completed sub-task\n6: commented\n7: approved\n8: changed date\n9: changed time\n1:> changed location\n1:> rejected \n1:> started',
  `action_time` DOUBLE(13,3) NOT NULL DEFAULT '0.000',
  `assignees` TEXT NOT NULL,
  `content` VARCHAR(1000) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL DEFAULT '' COMMENT 'content changes',
  `tag_name` VARCHAR(50) NOT NULL DEFAULT '',
  `kanban_id` BIGINT NOT NULL DEFAULT '0',
  `kanban_card_id` BIGINT NOT NULL DEFAULT '0',
  `created_date` DOUBLE(13,3) NOT NULL DEFAULT '0.000',
  `updated_date` DOUBLE(13,3) NOT NULL DEFAULT '0.000',
  `category` TINYINT(1) NOT NULL DEFAULT '0',
  `collection_id` BIGINT NOT NULL DEFAULT '-1',
  PRIMARY KEY (`id`),
  KEY `collection_history_collection_id_IDX` (`collection_id`) USING BTREE,
  KEY `collection_history_collection_id_updated_date_IDX` (`collection_id`,`updated_date`) USING BTREE,
  KEY `idx_activity_id` (`collection_activity_id`),
  KEY `idx_email` (`email`),
  KEY `idx_updated_date` (`updated_date`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1