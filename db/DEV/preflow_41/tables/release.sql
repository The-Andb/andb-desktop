CREATE TABLE `release` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
  `user_id` BIGINT UNSIGNED NOT NULL COMMENT 'Just tracking who upload/create this release Flo app',
  `version` VARCHAR(25) CHARACTER SET latin1 COLLATE latin1_swedish_ci NOT NULL DEFAULT '' COMMENT 'Version Flo app released',
  `checksum` VARCHAR(200) CHARACTER SET latin1 COLLATE latin1_swedish_ci NOT NULL DEFAULT '' COMMENT 'It is string and it will be hashed by client side',
  `release_note` TEXT COLLATE utf8mb4_general_ci COMMENT 'Admin can note anything on this field',
  `description` TEXT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci,
  `file_name` VARCHAR(200) COLLATE utf8mb4_general_ci NOT NULL COMMENT 'This is real name of file which uploaded to server side (it will be shown on the UI of Admin page)',
  `file_uid` VARCHAR(100) COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT 'It is ID of file which stored in HDD (it will be generated by server side)',
  `app_id` VARCHAR(255) COLLATE utf8mb4_general_ci NOT NULL COMMENT 'Flo app type, default =0 : flo mac app (refer to table app_register)',
  `build_number` DECIMAL(16,0) NOT NULL COMMENT 'Unique number that marks the application build number.',
  `title` VARCHAR(128) COLLATE utf8mb4_general_ci NOT NULL DEFAULT '',
  `message` VARCHAR(512) COLLATE utf8mb4_general_ci NOT NULL DEFAULT '',
  `expire_date` DOUBLE(13,3) DEFAULT '0.000' COMMENT 'Time expire of this build',
  `message_expire` VARCHAR(5000) COLLATE utf8mb4_general_ci NOT NULL DEFAULT '',
  `os_support` VARCHAR(45) COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT 'Minimum operating system supported for installation',
  `length` INT NOT NULL COMMENT 'The size of the zip file is uploaded',
  `file_dsym` VARCHAR(100) COLLATE utf8mb4_general_ci NOT NULL COMMENT 'Key check reports application crashes',
  `file_dsym_uid` VARCHAR(100) COLLATE utf8mb4_general_ci DEFAULT NULL,
  `length_dsym` INT NOT NULL COMMENT 'The size of the dsym file is uploaded',
  `url_download` VARCHAR(512) COLLATE utf8mb4_general_ci DEFAULT NULL,
  `release_type` enum('0','1') COLLATE utf8mb4_general_ci NOT NULL DEFAULT '0' COMMENT 'Value: \n\n  * 0: Minor: normal update\n  * 1: Major: any build that requires a removal of existing data and reinstall.',
  `release_time` DOUBLE(13,3) DEFAULT NULL,
  `release_status` enum('0','1','2','3') CHARACTER SET utf8mb4 COLLATE utf8mb4_general_ci DEFAULT NULL COMMENT 'Value: \n  * 0: admin-upload\n  * 1: Media Upload',
  `upload_type` enum('0','1') COLLATE utf8mb4_general_ci DEFAULT '0',
  `upload_status` TINYINT(1) DEFAULT NULL,
  `created_date` DOUBLE(13,3) NOT NULL,
  `updated_date` DOUBLE(13,3) DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `app_id` (`app_id`),
  KEY `checksum` (`checksum`),
  KEY `created_date` (`created_date`),
  UNIQUE KEY `file_dsym_uid_UNIQUE` (`file_dsym_uid`),
  KEY `file_name` (`file_name`),
  UNIQUE KEY `file_uid_UNIQUE` (`file_uid`),
  KEY `os_support` (`os_support`),
  KEY `release_status` (`release_status`),
  KEY `release_time` (`release_time`),
  KEY `release_type` (`release_type`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci